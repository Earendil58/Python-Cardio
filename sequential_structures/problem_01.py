#Read two numbers and calculates: the sum and the average of the two. 

first_number = float(input('Enter the first number: '))
second_number = float(input('Enter the second number: '))

sum_two_numbers = first_number + second_number
average_two_numbers = (first_number + second_number) / 2

print(f'The sum is {sum_two_numbers}')
print(f'The average is {average_two_numbers}')